enum Gender: String, Codable {
	case agender = "Agender"
	case bigender = "Bigender"
	case female = "Female"
	case genderfluid = "Genderfluid"
	case genderqueer = "Genderqueer"
	case male = "Male"
	case nonBinary = "Non-binary"
	case polygender = "Polygender"
}
